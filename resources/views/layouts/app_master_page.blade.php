<script>
    $('#key').keyup(function () {
        let query = $(this).val();
        let url = '{{route('page.home.autoComplete')}}';
        //alert(query);
        if (query != '') {
            $.ajax({
                headers: {
                    'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                },
                method: "POST",
                url: url,
                data: {query: query}
            })
                .done(function (msg) {
                    $('#complete_key').html(msg);
                });
        } else
            $('#complete_key').fadeOut();
    });
    $(document).on('click', '.ajax_click', function (event) {
        event.preventDefault();
        $('#key').val($(this).text());
        $('#complete_key').fadeOut();
    });
</script>
